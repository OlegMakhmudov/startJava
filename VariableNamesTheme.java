        int initialNumber = 10;
        System.out.println("Исходное число = " + initialNumber);

        // Процент по вкладу
        double interestRate = 6.3;
        System.out.println("Процент по вкладу = " + interestRate);

        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ = " + ampersand);

        // Код ошибки
        int codeError = 404;
        System.out.println("Код ошибки = " + codeError);

        // Тип сообщения
        String messageType = "Тип сообщения";
        System.out.println("Тип сообщения = " + messageType);

        // Число нулей
        int countOfZeroes = 10;
        System.out.println("Число нулей = " + countOfZeroes);

        // Уникальное число
        int uniqueNumber = 7;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 8;
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int mathExpression = 5 + 5;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        double gameScore = 2.0;
        System.out.println("Счет в игре " + gameScore);

        // Максимальная длина
        int maxLength = 100;
        System.out.println("Максимальная длина = " + maxLength);

        // Пункт меню
        String menuItem = "Coffee";
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        double coffeeCost = 104.0;
        System.out.println("Стоимость кофе = " + coffeeCost);

        // Дата начала
        String startDate = "2024-20-10";
        System.out.println("Дата начала = " + startDate);

        // Окончание диапазона
        String endOfRange = "2024 год";
        System.out.println("Окончание диапазона" + endOfRange);


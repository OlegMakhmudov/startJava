        String fullNameEmployeeOfMonth = "Чарл Чаплин";
        System.out.println("Полное имя работника месяца = " + fullNameEmployeeOfMonth);

        // Заголовок электронной книги
        String ebookTitle = "Микросервисная архитектура";
        System.out.println("Заголовок электронной книги = " + ebookTitle);

        // Размер
        int size = 52;
        System.out.println("Размер = " + size);

        // Счетчик
        int counter = 10;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        String pathToFile = "C:/Work/JavaStart/Lesson_1/VariableNamesTheme.java";
        System.out.println("Путь до файла = " + pathToFile);

        // Количество чисел в каждой строке
        int countNumbersPerLine = 5;
        System.out.println("Количество чисел в каждой строке = " + countNumbersPerLine + "\n");

        System.out.println("2. Boolean-переменные\n");

        // Сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны? = " + isEqualHundreds);

        // Компьютер включен?
        boolean isComputerOn = true;
        System.out.println("Компьютер включен? = " + isComputerOn);

        // Есть равные цифры?
        boolean hasEqualNumbers = true;
        System.out.println("Есть равные цифры? = " + hasEqualNumbers);

